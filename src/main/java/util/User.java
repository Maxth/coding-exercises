package util;

public class User {

    private int id;
    String firstname;
    String lastname;
    String username;
    int age;

    public User() {
    }

    public String getFirstname() {
        return firstname;
    }

    public int getId() {
        return id;
    }

    public String getLastname() {
        return lastname;
    }

    public String getUsername() {
        return username;
    }

    public int getAge() {
        return age;
    }
}